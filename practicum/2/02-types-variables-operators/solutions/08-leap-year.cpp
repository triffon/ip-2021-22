#include <iostream>

using namespace std;

/**
 * Въведете от конзолата година, проверете дали е високосна.
 * Ако е високосна изведете 1, ако не е - 0.
 */
int main()
{
    short year;
    cin >> year;

    // Ако годината се дели на 4, но не се дели на 100 - то тя е високосна
    // Ако се дели на 400 също е високосна, без значение от другите условия.
    bool isLeapYear = ((year % 4 == 0) && (year % 100 != 0)) || (year % 400 == 0);

    cout << isLeapYear;

    return 0;
}
